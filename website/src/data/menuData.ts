export type SpiceLevel = "mild" | "medium" | "hot";

export interface ModifierOption {
  name: string;
  price: number; // add-on price; 0 = no upcharge
}

export interface ModifierGroup {
  slug: string;
  name: string;
  required: boolean;
  min: number;
  max: number;
  multiDifferent: boolean; // may pick several distinct options
  multiSame: boolean;      // may pick the same option more than once
  options: ModifierOption[];
}

export interface MenuItem {
  id: string;
  name: string;
  description: string;
  price: number;
  image: string;
  spiceLevel: SpiceLevel;
  category: string;
  featured?: boolean; // staff-flagged "Best Seller", drives the homepage section
  modifierGroups?: string[]; // slugs into modifierGroups
}

export interface MenuCategory {
  id: string;
  name: string;
  description: string;
  items: MenuItem[];
}

